    private void jButton1ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ConsultarActionPerformed
        if (this.jComboBox1idcliente.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor Ingrese el numero de cedula del cliente  a consultar");
        } else {
            int idcliente = Integer.parseInt(this.jComboBox1idcliente.getSelectedItem().toString());
            Facturas_ClientesDao d = new Facturas_Clientes_Implements();
            Entidad_Facturav h = new Entidad_Facturav(idcliente);
            d.ExtraerFactura(h);
            this.jTextFieldcodigofacturav.setText(String.valueOf(h.getIdfactura()));
            this.jTextFieldfromadepago.setText(h.getFormapago());
            this.jDateChooser1fecha.setDate(h.getFecha());

            mostraTablaMsql();
            this.jButton1Consultar.setEnabled(false);

        }
    }//GEN-LAST:event_jButton1ConsultarActionPerformed

