    public void agregar() {
        modelo.addRow(new Object[]{
            this.jtexfieldnumproveedor.getText(), this.JtexFieldnombre.getText(), this.jTextFieldpaginaweb.getText(),
            this.jTextFieldtelefono.getText(), this.jTextFielddireccion.getText(), this.jTextFieldcorreo.getText()});
        this.jTabletabla.setModel(modelo);
    }
    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed

        if (this.jtexfieldnumproveedor.getText().equals("") || this.JtexFieldnombre.getText().equals("") || this.jTextFieldpaginaweb.getText().equals("")
                || this.jTextFielddireccion.getText().equals("") || this.jTextFieldtelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, " Por favor ingresar los datos de todos los campos ");
        } else {
            //si la tabla esta vacia , osea es el primer elemento a ingresar
            if (this.jTabletabla.getRowCount() == 0) {
                int id_proveedor = Integer.parseInt(this.jtexfieldnumproveedor.getText());
                String nombre = this.JtexFieldnombre.getText();
                String paginaweb = this.jTextFieldpaginaweb.getText();
                String telefono = this.jTextFieldtelefono.getText();
                String direccion = this.jTextFielddireccion.getText();
                String correo = this.jTextFieldcorreo.getText();
                JOptionPane.showMessageDialog(null, "El Proveedor" + nombre + " con codigo " + id_proveedor + " se registro correctamente ");
                ProveedorDao ProveedorDao = new Proveedorimplement();
                Entidad_Proveedor EntidadProveedor = new Entidad_Proveedor(id_proveedor, nombre, paginaweb, telefono, direccion, correo);
                ProveedorDao.Insertar(EntidadProveedor);
                agregar();
                limpiar();
            } else {

                boolean n = false, p = false;
                for (int i = 0; i < this.jTabletabla.getRowCount(); i++) { // recorro la tabla para verificar que el producto a ingresa no sea el mismo 

                    if (jTabletabla.getValueAt(i, 0).toString().equals(this.jtexfieldnumproveedor.getText())) { // verifico el producto al igual que ya esta registrado 1 que es la colunna del producto
                        p = true;
                    }

                    if (jTabletabla.getValueAt(i, 1).equals(this.JtexFieldnombre.getText())) {
                        n = true;

                    }

                }
                if (n == true || p == true) {
                    JOptionPane.showMessageDialog(null, " El Codigo y/o Proveedor ya esta registrado  ");
                } else {

                    int id_proveedor = Integer.parseInt(this.jtexfieldnumproveedor.getText());
                    String nombre = this.JtexFieldnombre.getText();
                    String paginaweb = this.jTextFieldpaginaweb.getText();
                    String telefono = this.jTextFieldtelefono.getText();
                    String direccion = this.jTextFielddireccion.getText();
                    String correo = this.jTextFieldcorreo.getText();
                    JOptionPane.showMessageDialog(null, "El Proveedor" + nombre + " con codigo " + id_proveedor + " se registro correctamente ");
                    ProveedorDao ProveedorDao = new Proveedorimplement();
                    Entidad_Proveedor EntidadProveedor = new Entidad_Proveedor(id_proveedor, nombre, paginaweb, telefono, direccion, correo);
                    ProveedorDao.Insertar(EntidadProveedor);
                    agregar();
                    limpiar();
                }
            }
        }
    }//GEN-LAST:event_RegistrarActionPerformed

    private void jButton2eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2eliminarActionPerformed
        ProveedorDao Proveedordao = new Proveedorimplement();
        if (this.jtexfieldnumproveedor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor Ingrese la N째 del usuario a eliminar ");
        } else {
            int id = Integer.parseInt(this.jtexfieldnumproveedor.getText());
            Entidad_Proveedor Proveedor = new Entidad_Proveedor(id);
            Proveedordao.Eliminar(Proveedor);
            JOptionPane.showMessageDialog(null, "EL registro se elimino satisfactoriamente de la base de datos ");
            limpiar();
        }
    }//GEN-LAST:event_jButton2eliminarActionPerformed

