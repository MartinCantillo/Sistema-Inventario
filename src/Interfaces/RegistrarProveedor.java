    private void jButton1BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1BuscarActionPerformed
        ProveedorDao ProveedorDao = new Proveedorimplement();
        if (this.jtexfieldnumproveedor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor Ingrese el codigo del usuario a buscar ");
        } else {
            int id_proveedor = Integer.parseInt(this.jtexfieldnumproveedor.getText());
            Entidad_Proveedor entidadProveedor = new Entidad_Proveedor(id_proveedor);
            ProveedorDao.buscar(entidadProveedor);
            this.jtexfieldnumproveedor.setText(String.valueOf(entidadProveedor.getIdProveedor()));
            this.JtexFieldnombre.setText(entidadProveedor.getNombreProveedor());
            this.jTextFieldpaginaweb.setText(entidadProveedor.getPaginaweb());
            this.jTextFielddireccion.setText(entidadProveedor.getDireccionProveedor());
            this.jTextFieldtelefono.setText(entidadProveedor.getTelefonoProveedor());
            this.jTextFieldcorreo.setText(entidadProveedor.getCorreoElectronico());
            JOptionPane.showMessageDialog(null, " Registro encontrado ");
        }
        limpiar();
    }//GEN-LAST:event_jButton1BuscarActionPerformed

